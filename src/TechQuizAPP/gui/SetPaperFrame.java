    private void btnSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetActionPerformed
String subject =jcLanguage.getSelectedItem().toString();
int totalQuest =Integer.parseInt(jcQuestions.getSelectedItem().toString());
int ans;
ans= JOptionPane.showConfirmDialog(null, "You have choosen\""+subject+"\"and\""+totalQuest+"\"question!)\n is this ok?","Confirmation!",    JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
if(ans==JOptionPane.YES_OPTION){
    try{
        String examId=ExamDAO.getExamId();
        ExamPojo newExam =new ExamPojo(examId,subject,totalQuest);
        SetQuestionFrame questionFrame = new SetQuestionFrame(newExam);
        questionFrame.setVisible(true);
        this.dispose();
    }
    catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "Error while connnecting to sql","Exception!",JOptionPane.ERROR_MESSAGE);
  ex.printStackTrace();
    }
}
    }//GEN-LAST:event_btnSetActionPerformed

