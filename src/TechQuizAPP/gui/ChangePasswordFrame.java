     public int validateInput(){
         userid  =txtUserId.getText().trim();
          char[]  pwd= txtPassword.getPassword();
        char[] rePassword=txtRePassword.getPassword();
          if(userid.isEmpty()||(pwd.length==0)||rePassword.length==0){
            return 0;
          }
           else{
              password=String.valueOf(pwd);
          }
           String repassword=String.valueOf(rePassword);
           if(userid.length()<4 || pwd.length<4)
               return -2;
           if(password.equals(repassword)==false)
                return -1;
           else
                return 1;
       }
