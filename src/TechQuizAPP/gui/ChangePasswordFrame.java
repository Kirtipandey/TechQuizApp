    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 if(validateInput()==0)
        {	
            JOptionPane.showMessageDialog(null, "Please fill all the input fields","Cannot Register Student!",JOptionPane.ERROR_MESSAGE);
            return;
        }
         if(validateInput()==-1)
        {	
            JOptionPane.showMessageDialog(null, "Please fill same password and repassword","Cannot Rester User!",JOptionPane.ERROR_MESSAGE);
            return;
        }    
         if(validateInput()==-2)
        {	
            JOptionPane.showMessageDialog(null, "user ID and passwrd should be mre than 4 char","Cannot Register User!",JOptionPane.ERROR_MESSAGE);
            return;
        } 
         try{
                          boolean status=UserDAO.changePassword(userid,password);
            if(status)
            {	
                JOptionPane.showMessageDialog(null, "Password Successfully Changed!","Userid: "+userid+"\nPassword:"+password,JOptionPane.INFORMATION_MESSAGE);
                StudentOptionFrame optionFrame=new StudentOptionFrame();
                optionFrame.setVisible(true);
                this.dispose();
            }
            else
            JOptionPane.showMessageDialog(null, "Password Not Changed!","Password Change Denied!",JOptionPane.ERROR_MESSAGE);

    }
        catch(SQLException ex)
        {
           JOptionPane.showMessageDialog(null, "Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE); 
           ex.printStackTrace();
        }
         
    }//GEN-LAST:event_jButton1ActionPerformed

